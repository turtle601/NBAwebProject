package com.js.nba.member;

public class Member {
	private String[] team;
	private int year;
	private int month;
	private int day;
	
	public Member() {
				
	}

	public String[] getTeam() {
		return team;
	}

	public void setTeam(String[] team) {
		this.team = team;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}


	
		
}
